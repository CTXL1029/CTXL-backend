function copy_anime_name() {
    const copy_anime_name = "\copy_anime_name";
    navigator.clipboard.writeText(copy_anime_name);
    window.open(copy_anime_name, "_blank");
}

function contact() {
    const contact = "\contact";
    navigator.clipboard.writeText(contact);
    window.open(contact, "_blank");
}

function download_sites() {
    const download_sites = "\copy_anime_name\download_sites";
    navigator.clipboard.writeText(download_sites);
    window.open(download_sites, "_blank");
}
