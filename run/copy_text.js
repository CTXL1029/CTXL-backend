/* Copy text into clipboard */

function copyText_010()
{   
    const name_010 = "Witch Watch";
    navigator.clipboard.writeText(name_010);
    alert("Đã sao chép tên bộ Anime:\n"+ name_010);
    setTimeout(() => {
        close();
        button.disabled = false;
    }, 2700);
}

