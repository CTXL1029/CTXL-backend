/* Copy text into clipboard */

function copyText_01()
{   
    const name_01 = "Chẳng Thể Lý Giải Nổi Aharen-san Mùa 2";
    navigator.clipboard.writeText(name_01);
    alert("Đã sao chép tên bộ Anime:\n"+ name_01);
    setTimeout(() => {
        close();
        button.disabled = false;
    }, 2700);
}

